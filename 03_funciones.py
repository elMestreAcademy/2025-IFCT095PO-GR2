# Definir función (mínimo)
# de froma implicita tiene un return None
def funcion_minima():
    # BLOQUE DE CÓDIGO
    pass


def funcion_completa(parametro_1, parametro_2):
    # bloque de código
    dato = parametro_1 + parametro_2

    return dato


# Llamar (invocar) a la función
funcion_minima()
funcion_completa(1, 3)

print("el resultado de funcion_completa(2, 3) es:", funcion_completa(2, 3))
